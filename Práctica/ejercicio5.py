# Ejercicio de de Listas y diccionarios
# Crea un programa que almacene en una lista que almacene los datos de un atleta olimpico.
# El programa debe pedir al usuario que introduzca el nombre del atleta, la edad, la disciplina que practica y el pais de origen.
# El programa debe almacenar los datos en un diccionario y añadirlo a la lista.
# El programa deberá también mostrar el mejor atleta por cada disciplina.